import UIKit

class Fonts {
    
    static func regular(size: CGFloat) -> UIFont {
        return .init(name: "Montserrat-Regular", size: size)!
    }
    
    static func medium(size: CGFloat) -> UIFont {
        return .init(name: "Montserrat-Medium", size: size)!
    }
    
    static func semiBold(size: CGFloat) -> UIFont {
        return .init(name: "Montserrat-SemiBold", size: size)!
    }
}
